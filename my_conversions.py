def convert_kilometers(miles):
    return miles * 1.60934


def convert_celsius(fahrenheit):
    return (fahrenheit - 32) * 5 / 9


def convert_liters(gallons):
    return gallons * 3.7854


def convert_kilograms(pounds):
    return pounds * 0.453592


def convert_centimeters(inches):
    return inches * 2.54